public class Pointer {

    int pointX;
    int pointY;

    public Pointer() {
    }

    public int getPointX() {
        return pointX;
    }

    public void setPointX(int pointX) {
        this.pointX = pointX;
    }

    public int getPointY() {
        return pointY;
    }

    public void setPointY(int pointY) {
        this.pointY = pointY;
    }
   
    
    public Pointer(int pointX, int pointY) {
        this.pointX = pointX;
        this.pointY = pointY;
      
    }

    
    
}
